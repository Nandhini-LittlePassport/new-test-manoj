
public class LoopingExample {

	public static void main(String[] args) {
		for(int i=0;i<=9;i++) {
			System.out.println("Hello for !!!");
		}
		int i=0;
		while(i<=9) {
			System.out.println("Hello while !!!");
			i++;
		}
		int j=0;
		do {
			System.out.println("hello");
			i++;
		}while(j>0);
	}

}
